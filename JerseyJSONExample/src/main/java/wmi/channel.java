package wmi;

public class channel {
    
    
    String URL;
    String Views;
    String Country;
    String Subscribers;
    String Videos;
    String Title;
    String Category;
    
    void setURL(String a){
        this.URL = a;
    }
    
    String getURL(){
        return URL;
        
    }
    
    void setViews(String a){
        this.Views = a;
    }
    
    String getViews(){
        return Views;
        
    }
    
    void setCountry(String a){
        this.Country = a;
    }
    
    String getCountry(){
        return Country;
        
    }
    
    void setSubscribers(String a){
        this.Subscribers = a;
    }
    
    String getSubscribers(){
        return Subscribers;
        
    }
    
    void setVideos(String a){
        this.Videos = a;
    }
    
    String getVideos(){
        return Videos;
        
    }
    
    void setTitle(String a){
        this.Title = a;
    }
    
    String getTitle(){
        return Title;
        
    }
    
    void setCategory(String a){
        this.Category = a;
    }
    
    String getCategory(){
        return Category;
        
    }
    
}
